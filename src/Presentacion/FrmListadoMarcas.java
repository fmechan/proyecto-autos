    @Override
    public final void loadTable(){
        try{
            
            for (int i=this.defaultTable.getRowCount()-1;i>=0;i--){
                this.defaultTable.removeRow(i);
            }
            
            Marcas clsMarcas = new Marcas();
            ArrayList arrayList = clsMarcas.getAll();
            
            for (int i = 0; i < arrayList.size(); i++) {
                EMarcas marca = (EMarcas)arrayList.get(i);
                this.defaultTable.addRow(new Object[]{});
                this.defaultTable.setValueAt(marca.getId(), i, 0);
                this.defaultTable.setValueAt(marca.getNombre(), i, 1);

            }
            
            EColorTableCell foreColor = new EColorTableCell();
            foreColor.setColumnIndex(1);
            tblMarcas.getColumnModel().getColumn(1).setCellRenderer(foreColor);
            
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString(), Module.titleMessage, JOptionPane.ERROR_MESSAGE);
        }
    }
